CREATE TABLE IF NOT EXISTS TREE (
  ID        INT(11)      NOT NULL AUTO_INCREMENT COMMENT 'Sequence',
  NAME      VARCHAR(250) NOT NULL
  COMMENT 'Name',
  TREE      VARCHAR(10)  NOT NULL
  COMMENT 'Tree Type',
  NODE      VARCHAR(10)  NOT NULL
  COMMENT 'Node Type',
  ROOT      BOOLEAN      NULL DEFAULT FALSE
  COMMENT 'Username',
  USERNAME  VARCHAR(50)  NOT NULL
  COMMENT 'Username',
  PARENT_ID INT(11) DEFAULT NULL
  COMMENT 'Parent',
